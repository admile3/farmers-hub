export function buildFlowerImagePrompt(flowerName, colors = "") {
  const colorNote = colors ? ` Typical colors: ${colors}.` : "";

  return `Photorealistic minimal stem study of ${flowerName}, shown as one clean representative cut stem in a small clear glass bud vase on a warm off-white background. Match a botanical catalog product image: centered composition, soft natural window light, shallow depth of field, neutral cream backdrop, realistic stem and foliage, accurate flower form, no text, no labels, no hands, no extra props, square crop.${colorNote}`;
}

export function getFlowerVisualByName(name) {
  if (!name) return null;
  const normalized = String(name).trim().toLowerCase();

  return (
    flowerVisualDatabase.find(
      (flower) => flower.name.toLowerCase() === normalized
    ) || null
  );
}

export function getFlowerVisualById(id) {
  if (!id) return null;
  return flowerVisualDatabase.find((flower) => flower.id === id) || null;
}

export function getFlowerImagePath(name) {
  return getFlowerVisualByName(name)?.path || "";
}
